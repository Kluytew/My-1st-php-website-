    <h2>What is a Data Analyst?</h2>
    <p>Are you interested in collecting, processing, and performing statistical analyses of data? You may want to consider a career as a data analyst!

Whether it be market research, sales figures, logistics, or transportation costs, every business collects data. Data analysts translate numbers and data into plain English in order to help organizations and companies understand how to make better business decisions.

A data analyst will take data and figure out a variety of things, such as how to price new materials, how to reduce transportation costs, or how to deal with issues that cost the company money.</p>
<h2>What does a Data Analyst do?</H2>
<img class="bd-placeholder-img" src="_4.jpg" height="400" width="400">
<p>Data analysts ascertain how data can be used in order to answer questions and solve problems. They study what’s happening now to identify trends and make predictions about the future. They are like detectives, figuring out how things work and helping to make sense of everything. It can be a creative, challenging and rewarding career.

Data analysts typically use computer systems and calculation applications to figure out their numbers. Data must be regulated, normalized, and calibrated so that it can be extracted, used alone, or put in with other numbers and still keep its integrity. Facts and numbers are the starting point, but what is most important is understanding what they mean and presenting the findings in an interesting way, using graphs, charts, tables, and graphics.

Data analysts need to have the ability to not only decipher data, but to report and explain what differences in numbers mean when looked at from year to year or across various departments. Because data analysts are often the ones with the best sense of why the numbers are the way they are, they are often asked to advise project managers and department heads concerning certain data points and how they can be changed or improved over a period of time. </P>
<h4>Data analysts may have the following responsibilities: </h4>
<h5>1. Working with technology teams, management and/or data scientists to set goals </h5>
<h5>2. Mining data from primary and secondary sources </h5>
<h5>3. Cleaning and dissecting data to get rid of irrelevant information </h5>
<h5>4. Analyzing and interpreting results using statistical tools and techniques </h5>
<h5>5. Pinpointing trends and patterns in data sets </h5>
<h5>6. Identifying new opportunities for process improvement  </h5>
<h5>7. Providing data reports for management </h5>
<h5>8. Designing, creating and maintaining databases and data systems </h5>
<h5>9. Fixing code problems and data-related issues </h5>
<h7>A data analyst's skills may not be as advanced as a data scientist's skills, but their goals are very similar. Data analysts are sometimes called “junior data scientists” and may be limited to handling specific business tasks using existing tools, systems, and data sets.</h7>
<h3>What is the workplace of a Data Analyst like?</h3>
<p>Data analysis is a highly transferable skill and can open the door to many interesting jobs across the private and public sector. Almost every industry imaginable has a need for data analysis; the fields of sales, marketing, and healthcare tend to have the most jobs available for these professionals at any given time.

Most data analysts work on teams, and a lot of the work is done on the computer. Much of the work can be done from home or from a remote office, though this sometimes depends on the type of data being gathered. Data analysts can typically expect to work 9-5 hours, however important projects or looming deadlines can and often require some overtime and weekend work.</p>
